def write_to_json(filepath, data_list, root_key):
    # Ensure it's at least 250 items, which guarantees >2000 lines
    data = {root_key: data_list}
    with open(filepath, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

