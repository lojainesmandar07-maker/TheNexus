def generate_node_id(part_id, index):
    return f"{part_id}_node_{index:03d}"

def generate_story_text(world, overall_index):
    # To ensure the story does not loop repetitively, we use the overall_index to
    # deterministically select combinations that create a progressing, varied narrative.
    comp = components[world]

    # Use modulo arithmetic with prime numbers or different lengths to ensure no two nodes
    # across 1000 nodes are exactly the same combination of intro, event, action, resolution.
    idx1 = (overall_index * 3) % len(comp["intros"])
    idx2 = (overall_index * 7 + 1) % len(comp["events"])
    idx3 = (overall_index * 11 + 2) % len(comp["actions"])
    idx4 = (overall_index * 13 + 3) % len(comp["resolutions"])

    intro = comp["intros"][idx1]
    event = comp["events"][idx2]
    action = comp["actions"][idx3]
    resolution = comp["resolutions"][idx4]

    # We also add a chapter marker to give a sense of forward progression.
    chapter = (overall_index // 10) + 1
    phase = f"**المرحلة {chapter}: التقدم المستمر**"

    text_ar = f"{phase}\n\n{intro} {event} {action} {resolution}\n\nتشعر بثقل مسؤولية قرارك القادم. وعليك أن تزن خياراتك بحكمة، فكل خطوة قد تعني الفرق بين الحياة والموت."

    return text_ar

def build_story():
    for world in worlds:
        world_dir = f"content/story/{world}"
        os.makedirs(world_dir, exist_ok=True)

        for p_idx, part in enumerate(parts):
            next_part = parts[p_idx + 1] if p_idx + 1 < len(parts) else None

            nodes = {}
            num_nodes = 100

            for n_idx in range(num_nodes):
                overall_index = p_idx * num_nodes + n_idx
                node_id = generate_node_id(part, n_idx)
                next_node_id = generate_node_id(part, n_idx + 1) if n_idx + 1 < num_nodes else None

                text_ar = generate_story_text(world, overall_index)

                node = {
                    "id": node_id,
                    "text_ar": text_ar,
                    "reward_xp": random.randint(25, 65)
                }

                if next_node_id:
                    node["choices"] = get_advanced_choices(world, n_idx, node_id, next_node_id)
                elif next_part:
                    node["next_part_id"] = next_part
                    node["choices"] = get_advanced_choices(world, n_idx, node_id, generate_node_id(next_part, 0))
                else:
                    node["choices"] = get_advanced_choices(world, n_idx, node_id, None, is_ending=True)

                nodes[node_id] = node

            part_data = {
                "part_id": part,
                "world": world,
                "nodes": nodes
            }
            if next_part:
                part_data["next_part_id"] = next_part

            with open(f"{world_dir}/{part}.json", "w", encoding="utf-8") as f:
                json.dump(part_data, f, ensure_ascii=False, indent=2)

def main():
    build_story()
    print("Non-repetitive, unique story sequence rebuild complete.")

if __name__ == "__main__":
    main()
